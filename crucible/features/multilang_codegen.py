"""
features/multilang_codegen.py
================================
Multi-language code generation bridge for the Crucible pipeline.

Takes the Python code generated by Stage 4 and uses the LLM to produce
equivalent implementations in TypeScript, Go, or Rust.  Each language
translation runs as an independent LLM task, preserving the original Python
as the authoritative reference.

Design principles
-----------------
* **Additive**: never modifies Stage 4 output; translations live in a
  separate ``code_<lang>/`` subdirectory within the run directory.
* **Best-effort**: if a file cannot be translated the error is logged and
  the next file is attempted — one failure never blocks the entire batch.
* **Scope-aware**: TypeScript and Go targets are fully supported; Rust is
  available as an experimental option (``MULTILANG_ENABLE_RUST=1``).
* **Token-efficient**: each file is translated independently so context
  windows are not exhausted on large codebases.

Supported language targets
--------------------------
* ``typescript`` — TypeScript 5.x, ESM, strict mode, no any
* ``go``         — Go 1.21+, idiomatic packages, error returns
* ``rust``       — Rust 2021 edition (opt-in via ``MULTILANG_ENABLE_RUST=1``)
* ``julia``      — Julia 1.9+, scientific computing focus (opt-in via ``MULTILANG_ENABLE_JULIA=1``)

Environment variables
---------------------
``MULTILANG_MAX_FILES``     — max files to translate (default 10)
``MULTILANG_MAX_CHARS``     — max chars per file sent to LLM (default 4 000)
``MULTILANG_ENABLE_RUST``   — set to ``1`` to enable Rust target (default off)
``MULTILANG_ENABLE_JULIA``  — set to ``1`` to enable Julia target (default off)

Usage::

    from crucible.features.multilang_codegen import (
        translate_run_to_languages,
        MultiLangConfig,
    )

    config = MultiLangConfig(
        languages=["typescript", "go"],
    )
    result = translate_run_to_languages(
        run_dir="/path/to/saved_projects/my_run",
        config=config,
        llm=my_llm,
    )
    for lang, files in result.files_written.items():
        print(f"{lang}: {len(files)} file(s) written")
"""
