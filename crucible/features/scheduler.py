@register('scheduler')
class SchedulerFeature(BaseFeature):
    """APScheduler-based recurring pipeline job scheduler config generator."""

    name = 'scheduler'
    label = 'Pipeline Job Scheduler'
    requires: list[str] = []

    def run(self, run_dir: str, config: FeatureConfig) -> FeatureResult:
        t0 = time.monotonic()
        try:
            result = generate_scheduler(run_dir)
            if result.get('skipped'):
                return FeatureResult(
                    feature=self.name,
                    success=True,
                    summary='Scheduler disabled (SCHEDULER_ENABLED != 1)',
                    details={'enabled': False},
                    duration_seconds=time.monotonic() - t0,
                )
            cron = result.get('cron', 'unknown')
            tz = result.get('timezone', 'UTC')
            summary = (
                f'Scheduler config generated: cron="{cron}" tz={tz}; '
                f'runner + systemd + Windows Task XML written'
            )
            return FeatureResult(
                feature=self.name,
                success=True,
                summary=summary,
                details={
                    'cron': cron,
                    'timezone': tz,
                    'runner_path': result.get('runner_path'),
                },
                duration_seconds=time.monotonic() - t0,
            )
        except ValueError as exc:
            return FeatureResult(
                feature=self.name,
                success=False,
                summary=f'Invalid SCHEDULER_CRON: {exc}',
                error=str(exc),
                duration_seconds=time.monotonic() - t0,
            )
        except Exception as exc:
            return FeatureResult(
                feature=self.name,
                success=False,
                summary=str(exc),
                error=str(exc),
                duration_seconds=time.monotonic() - t0,
            )
