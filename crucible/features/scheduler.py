"""
features/scheduler.py
======================
APScheduler-based recurring pipeline job scheduler configuration generator.

Generates three artefacts for deploying the Crucible pipeline as a recurring
scheduled job:

1. ``{run_dir}/scheduler_runner.py``
   — A standalone Python script using APScheduler BlockingScheduler that
     invokes ``run_crucible_enhanced.py run`` on the configured cron schedule.

2. ``{run_dir}/deployment/scheduler/quantsaas.service``
   — A systemd unit file for Linux deployment (compatible with systemd 232+).

3. ``{run_dir}/deployment/scheduler/quantsaas_task.xml``
   — A Windows Task Scheduler XML definition (compatible with Windows 7 /
     Server 2008 R2 and later).

4. ``{run_dir}/scheduler_config.json``
   — Job metadata: cron expression, timezone, next N fire times (estimated),
     and paths to generated files.

Environment variables
---------------------
SCHEDULER_ENABLED    '1' to enable (default: '1').
SCHEDULER_CRON       Cron expression (default: '0 8 * * 1-5' — weekdays 08:00 UTC).
SCHEDULER_TIMEZONE   Timezone name (default: 'UTC').
"""
from __future__ import annotations

import html
import json
import os
import re
import textwrap
import time
from typing import Any, Dict, List, Optional

from crucible.feature_registry import BaseFeature, FeatureConfig, FeatureResult, register

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _env(name: str, default: str = '') -> str:
    # Fall back to default when the variable is absent OR explicitly set to "".
    # os.environ.get(name, default) only uses default when the key is absent;
    # an empty-string value (e.g. SCHEDULER_ENABLED="") would otherwise disable
    # the scheduler even though the documented default is "1" (enabled).
    v = os.environ.get(name, default).strip()
    return v if v else default


_CRON_FIELD_RE = re.compile(r'^[\d*,/\-]+$')
# Timezone names must be standard IANA identifiers: letters, digits, /,  _, +, -
# (e.g. "UTC", "America/New_York", "Etc/GMT+5").  This allowlist prevents code/
# config injection when the value is interpolated into generated source files.
_TZ_VALID_RE = re.compile(r'^[A-Za-z][A-Za-z0-9/_+\-]{0,63}$')


def _validate_timezone(tz: str) -> str:
    """Return *tz* unchanged if it looks like a valid IANA timezone name.

    Raises ``ValueError`` for values that could inject content into generated
    Python source files or systemd unit files.
    """
    if not _TZ_VALID_RE.match(tz):
        raise ValueError(
            f"Invalid SCHEDULER_TIMEZONE '{tz}': must match "
            r"[A-Za-z][A-Za-z0-9/_+-]{0,63}"
        )
    return tz


def _parse_cron(expr: str) -> Dict[str, str]:
    """Split a 5-field cron expression into APScheduler kwargs.

    Fields: minute hour day_of_month month day_of_week
    """
    parts = expr.split()
    if len(parts) != 5:
        raise ValueError(
            f"Invalid cron expression '{expr}': expected 5 space-separated fields "
            "(minute hour day_of_month month day_of_week)"
        )
    keys = ['minute', 'hour', 'day', 'month', 'day_of_week']
    for key, val in zip(keys, parts):
        if not _CRON_FIELD_RE.match(val):
            raise ValueError(
                f"Invalid cron field {key}='{val}': only digits, *, , / and - are allowed"
            )
    return dict(zip(keys, parts))


def _cron_to_apscheduler_kwargs(expr: str) -> str:
    """Return the APScheduler CronTrigger keyword-argument string for *expr*."""
    fields = _parse_cron(expr)
    parts = [f"{k}='{v}'" for k, v in fields.items() if v != '*']
    return ', '.join(parts) if parts else "# every minute"


