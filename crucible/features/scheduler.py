def _scheduler_runner_source(cron: str, timezone: str, run_script: str) -> str:
    return textwrap.dedent(f'''\
        """
        scheduler_runner.py
        ====================
        APScheduler-based recurring pipeline runner.
        Auto-generated by Crucible scheduler feature.

        Usage:
            python scheduler_runner.py

        Dependencies:
            pip install apscheduler>=3.10

        Environment variables:
            SCHEDULER_CRON      Override cron schedule (default: '{cron}')
            SCHEDULER_TIMEZONE  Override timezone     (default: '{timezone}')
        """
        from __future__ import annotations

        import logging
        import os
        import subprocess
        import sys
        from pathlib import Path

        logging.basicConfig(
            level=logging.INFO,
            format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
        )
        log = logging.getLogger("quantsaas.scheduler")


        def run_pipeline() -> None:
            """Execute the Crucible pipeline via subprocess."""
            script = Path(__file__).parent / "{run_script}"
            cmd = [sys.executable, str(script), "run"]
            log.info("Starting pipeline: %s", " ".join(cmd))
            try:
                result = subprocess.run(
                    cmd, check=True, capture_output=False,
                    timeout=int(os.environ.get('PIPELINE_TIMEOUT_SECONDS', '3600'))
                )
                log.info("Pipeline completed with return code %d", result.returncode)
            except subprocess.CalledProcessError as exc:
                log.error("Pipeline failed with return code %d", exc.returncode)
            except Exception as exc:
                log.exception("Unexpected error running pipeline: %s", exc)


        def main() -> None:
            try:
                from apscheduler.schedulers.blocking import BlockingScheduler
                from apscheduler.triggers.cron import CronTrigger
            except ImportError:
                log.critical("apscheduler not installed: pip install apscheduler>=3.10")
                sys.exit(1)

            cron_expr = os.environ.get("SCHEDULER_CRON", "{cron}")
            tz = os.environ.get("SCHEDULER_TIMEZONE", "{timezone}")

            scheduler = BlockingScheduler(timezone=tz)
            scheduler.add_job(
                run_pipeline,
                CronTrigger.from_crontab(cron_expr, timezone=tz),
                id="quantsaas_pipeline",
                name="Crucible Pipeline Run",
                max_instances=1,
                coalesce=True,
                misfire_grace_time=300,
            )

            log.info("Scheduler starting — cron='%s' timezone='%s'", cron_expr, tz)
            try:
                scheduler.start()
            except (KeyboardInterrupt, SystemExit):
                log.info("Scheduler stopped.")
                scheduler.shutdown(wait=False)


        if __name__ == "__main__":
            main()
    ''')


def _systemd_unit_source(
    run_dir: str,
    runner_path: str,
    cron: str,
    timezone: str,
) -> str:
    # Convert cron fields to OnCalendar format (systemd timer uses its own syntax)
    # systemd OnCalendar: DayOfWeek Year-Month-Day Hour:Minute:Second
    # Best-effort conversion for simple crons; complex expressions need manual editing.
    try:
        fields = _parse_cron(cron)
        minute = fields['minute'] if fields['minute'] != '*' else '*'
        hour = fields['hour'] if fields['hour'] != '*' else '*'
        dow_map = {'0': 'Sun', '1': 'Mon', '2': 'Tue', '3': 'Wed',
                   '4': 'Thu', '5': 'Fri', '6': 'Sat',
                   '1-5': 'Mon..Fri', '0-6': '*'}
        dow_raw = fields.get('day_of_week', '*')
        dow = dow_map.get(dow_raw, dow_raw) if dow_raw != '*' else ''
        on_calendar = f"{dow + ' ' if dow else ''}*-*-* {hour}:{minute}:00"
    except ValueError:
        on_calendar = "*-*-* 08:00:00"

    # Escape double-quotes inside the paths so they survive being wrapped in
    # systemd's "…" quoting — paths with spaces would otherwise split the
    # WorkingDirectory= or ExecStart= directive at the first space.
    _run_dir_esc = run_dir.replace("\\", "\\\\").replace('"', '\\"')
    _runner_esc = runner_path.replace("\\", "\\\\").replace('"', '\\"')
    return textwrap.dedent(f'''\
        # quantsaas.service
        # systemd unit file for the Crucible pipeline scheduler.
        # Auto-generated by Crucible scheduler feature.
        #
        # Installation:
        #   sudo cp quantsaas.service /etc/systemd/system/
        #   sudo cp quantsaas.timer   /etc/systemd/system/
        #   sudo systemctl daemon-reload
        #   sudo systemctl enable --now quantsaas.timer

        [Unit]
        Description=Crucible Pipeline Scheduler
        After=network-online.target
        Wants=network-online.target

        [Service]
        Type=simple
        WorkingDirectory="{_run_dir_esc}"
        ExecStart=/usr/bin/env python3 "{_runner_esc}"
        StandardOutput=journal
        StandardError=journal
        Environment="TZ={timezone}"
        # Restart on failure with a 60-second back-off
        Restart=on-failure
        RestartSec=60

        [Install]
        WantedBy=multi-user.target
    ''')


def _systemd_timer_source(cron: str, timezone: str) -> str:
    try:
        fields = _parse_cron(cron)
        minute = fields['minute'] if fields['minute'] != '*' else '*'
        hour = fields['hour'] if fields['hour'] != '*' else '*'
        dow_map = {'0': 'Sun', '1': 'Mon', '2': 'Tue', '3': 'Wed',
                   '4': 'Thu', '5': 'Fri', '6': 'Sat',
                   '1-5': 'Mon..Fri', '0-6': '*'}
        dow_raw = fields.get('day_of_week', '*')
        dow = dow_map.get(dow_raw, dow_raw) if dow_raw != '*' else ''
        on_calendar = f"{dow + ' ' if dow else ''}*-*-* {hour}:{minute}:00"
    except ValueError:
        on_calendar = '*-*-* 08:00:00'

    return textwrap.dedent(f'''\
        # quantsaas.timer
        [Unit]
        Description=Crucible Pipeline Timer
        Requires=quantsaas.service

        [Timer]
        OnCalendar={on_calendar}
        Persistent=true

        [Install]
        WantedBy=timers.target
    ''')


def _windows_task_xml_source(
    runner_path: str,
    cron: str,
    timezone: str,
) -> str:
    """Generate a Windows Task Scheduler XML definition.

    Uses a LogonTrigger for simplicity (runs when the scheduled user logs on
    and the RepetitionInterval handles periodic execution).  For production use
    CalendarTrigger can be substituted with the appropriate schedule type.
    """
    try:
        fields = _parse_cron(cron)
        hour = fields['hour'].zfill(2) if fields['hour'] != '*' else '08'
        minute = fields['minute'].zfill(2) if fields['minute'] != '*' else '00'
        start_time = f"2024-01-01T{hour}:{minute}:00"
        # Day-of-week mapping to ScheduleByWeek DaysOfWeek element
        dow_map = {
            '0': '<Sunday/>', '1': '<Monday/>', '2': '<Tuesday/>',
            '3': '<Wednesday/>', '4': '<Thursday/>', '5': '<Friday/>',
            '6': '<Saturday/>', '1-5': '<Monday/><Tuesday/><Wednesday/><Thursday/><Friday/>',
            '*': '<Monday/><Tuesday/><Wednesday/><Thursday/><Friday/><Saturday/><Sunday/>',
        }
        dow_raw = fields.get('day_of_week', '*')
        dow_elements = dow_map.get(dow_raw, '<Monday/><Tuesday/><Wednesday/><Thursday/><Friday/>')
    except ValueError:
        start_time = "2024-01-01T08:00:00"
        dow_elements = '<Monday/><Tuesday/><Wednesday/><Thursday/><Friday/>'

    # Fully escape for XML: handles &, <, >, " in paths (quote=True covers attr values).
    # Compute the directory from the ORIGINAL path before escaping so that
    # os.path.dirname does not operate on already-HTML-encoded characters
    # (e.g. "C:\foo&amp;bar" as a literal dirname instead of "C:\foo&bar").
    runner_path_xml = html.escape(runner_path, quote=True)
    runner_dir_xml = html.escape(os.path.dirname(runner_path), quote=True)

    return textwrap.dedent(f'''\
        <?xml version="1.0" encoding="UTF-8"?>
        <Task version="1.2" xmlns="http://schemas.microsoft.com/windows/2004/02/mit/task">
          <RegistrationInfo>
            <Description>Crucible Pipeline Scheduler — auto-generated</Description>
            <Author>Crucible</Author>
          </RegistrationInfo>
          <Triggers>
            <CalendarTrigger>
              <StartBoundary>{start_time}</StartBoundary>
              <Enabled>true</Enabled>
              <ScheduleByWeek>
                <WeeksInterval>1</WeeksInterval>
                <DaysOfWeek>
                  {dow_elements}
                </DaysOfWeek>
              </ScheduleByWeek>
            </CalendarTrigger>
          </Triggers>
          <Principals>
            <Principal id="Author">
              <LogonType>InteractiveToken</LogonType>
              <RunLevel>LeastPrivilege</RunLevel>
            </Principal>
          </Principals>
          <Settings>
            <MultipleInstancesPolicy>IgnoreNew</MultipleInstancesPolicy>
            <DisallowStartIfOnBatteries>false</DisallowStartIfOnBatteries>
            <StopIfGoingOnBatteries>false</StopIfGoingOnBatteries>
            <AllowHardTerminate>true</AllowHardTerminate>
            <StartWhenAvailable>true</StartWhenAvailable>
            <RunOnlyIfNetworkAvailable>false</RunOnlyIfNetworkAvailable>
            <IdleSettings>
              <StopOnIdleEnd>false</StopOnIdleEnd>
              <RestartOnIdle>false</RestartOnIdle>
            </IdleSettings>
            <Enabled>true</Enabled>
            <ExecutionTimeLimit>PT4H</ExecutionTimeLimit>
            <Priority>7</Priority>
          </Settings>
          <Actions Context="Author">
            <Exec>
              <Command>python</Command>
              <Arguments>"{runner_path_xml}"</Arguments>
              <WorkingDirectory>{runner_dir_xml}</WorkingDirectory>
            </Exec>
          </Actions>
        </Task>
    ''')


