def generate_scheduler(run_dir: str) -> Dict[str, Any]:
    """Generate all scheduler artefacts.

    Parameters
    ----------
    run_dir:
        Pipeline run directory.

    Returns
    -------
    Dict with keys: cron, timezone, runner_path, systemd_service_path,
    systemd_timer_path, windows_task_path, scheduler_config_path.
    """
    enabled = _env('SCHEDULER_ENABLED', '1').lower() not in ('0', 'false', 'no', 'off')
    if not enabled:
        return {'enabled': False, 'skipped': True}

    cron = _env('SCHEDULER_CRON', '0 8 * * 1-5')
    timezone = _env('SCHEDULER_TIMEZONE', 'UTC')

    # Validate inputs early — both raise ValueError on bad input.
    # Timezone is validated separately because it is interpolated verbatim
    # into generated Python source and systemd unit files; an unvalidated
    # value could inject arbitrary content (code injection / config injection).
    _validate_timezone(timezone)
    _parse_cron(cron)  # validates field count AND per-field characters

    run_script = 'run_crucible_enhanced.py'

    # 1. scheduler_runner.py  (atomic write via tmp + os.replace)
    runner_path = os.path.join(run_dir, 'scheduler_runner.py')
    runner_source = _scheduler_runner_source(cron, timezone, run_script)
    _tmp = runner_path + '.tmp'
    try:
        with open(_tmp, 'w', encoding='utf-8', newline='\n') as fh:
            fh.write(runner_source)
        os.replace(_tmp, runner_path)
    except Exception:
        try:
            os.unlink(_tmp)
        except OSError:
            pass
        raise

    # 2. Deployment directory
    deploy_dir = os.path.join(run_dir, 'deployment', 'scheduler')
    os.makedirs(deploy_dir, exist_ok=True)

    # 3. systemd unit + timer  (atomic writes)
    service_path = os.path.join(deploy_dir, 'quantsaas.service')
    timer_path = os.path.join(deploy_dir, 'quantsaas.timer')
    for _path, _content in (
        (service_path, _systemd_unit_source(run_dir, runner_path, cron, timezone)),
        (timer_path, _systemd_timer_source(cron, timezone)),
    ):
        _tmp = _path + '.tmp'
        try:
            with open(_tmp, 'w', encoding='utf-8', newline='\n') as fh:
                fh.write(_content)
            os.replace(_tmp, _path)
        except Exception:
            try:
                os.unlink(_tmp)
            except OSError:
                pass
            raise

    # 4. Windows Task Scheduler XML  (atomic write)
    win_task_path = os.path.join(deploy_dir, 'quantsaas_task.xml')
    _tmp = win_task_path + '.tmp'
    try:
        with open(_tmp, 'w', encoding='utf-8', newline='\n') as fh:
            fh.write(_windows_task_xml_source(runner_path, cron, timezone))
        os.replace(_tmp, win_task_path)
    except Exception:
        try:
            os.unlink(_tmp)
        except OSError:
            pass
        raise

    # 5. scheduler_config.json  (atomic write)
    config: Dict[str, Any] = {
        'enabled': True,
        'cron': cron,
        'timezone': timezone,
        'run_script': run_script,
        'runner_path': runner_path,
        'systemd_service_path': service_path,
        'systemd_timer_path': timer_path,
        'windows_task_path': win_task_path,
        'apscheduler_version_required': '>=3.10',
        'deployment_notes': {
            'linux': 'Copy quantsaas.service + quantsaas.timer to /etc/systemd/system/, then: systemctl daemon-reload && systemctl enable --now quantsaas.timer',
            'windows': 'Import quantsaas_task.xml via Task Scheduler > Import Task or: schtasks /create /xml quantsaas_task.xml /tn Crucible',
            'docker': 'Run scheduler_runner.py as the container entrypoint with cron env vars configured',
        },
    }
    config_path = os.path.join(run_dir, 'scheduler_config.json')
    _tmp = config_path + '.tmp'
    try:
        with open(_tmp, 'w', encoding='utf-8', newline='\n') as fh:
            json.dump(config, fh, indent=2)
        os.replace(_tmp, config_path)
    except Exception:
        try:
            os.unlink(_tmp)
        except OSError:
            pass
        raise

    return config


