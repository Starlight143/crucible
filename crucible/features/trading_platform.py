@register('trading_platform')
class TradingPlatformFeature(BaseFeature):
    """Generates Alpaca, Binance, and IBKR trading adapter stubs."""

    name = 'trading_platform'
    label = 'Trading Platform Adapters'
    requires: list[str] = []

    def run(self, run_dir: str, config: FeatureConfig) -> FeatureResult:
        t0 = time.monotonic()
        try:
            report = generate_trading_platform(run_dir)
            n_files = len(report.get('generated_files', []))
            strategy = report.get('strategy_type', 'unknown')
            paper = report.get('paper_mode', True)
            summary = (
                f'Trading platform: {n_files} adapter(s) generated '
                f'for {strategy} strategy '
                f'(paper_mode={paper})'
            )
            return FeatureResult(
                feature=self.name,
                success=True,
                summary=summary,
                details={
                    'strategy_type': strategy,
                    'adapters_generated': n_files,
                    'paper_mode': paper,
                },
                duration_seconds=time.monotonic() - t0,
            )
        except Exception as exc:
            return FeatureResult(
                feature=self.name,
                success=False,
                summary=str(exc),
                error=str(exc),
                duration_seconds=time.monotonic() - t0,
            )
