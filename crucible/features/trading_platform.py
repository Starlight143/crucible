def generate_trading_platform(run_dir: str) -> Dict[str, Any]:
    """Detect strategy type, write adapter files, produce report.

    Parameters
    ----------
    run_dir:
        Pipeline run directory.

    Returns
    -------
    Dict with keys: strategy_type, targets, generated_files, paper_mode.
    """
    targets_env = os.environ.get('TRADING_PLATFORM_TARGETS', 'alpaca,binance').strip()
    # Fail-closed: paper trading is the safe default. The generated adapter source
    # files use this flag to gate place_order(); if a typo/unknown value silently
    # flipped paper_mode to False the generated code would submit *real* orders
    # against *real* funds. Only an explicit, recognised "off" token disables it.
    # Per CLAUDE.md (高風險系統加強 + env-bool whitelist): unknown values must
    # default to the SAFE side, which here is paper_mode=True.
    paper_mode_raw = os.environ.get('TRADING_PAPER_MODE', 'true').strip().lower()
    paper_mode = paper_mode_raw not in ('0', 'false', 'no', 'off')

    targets = [t.strip().lower() for t in targets_env.split(',') if t.strip()]
    strategy_type = detect_strategy_type(run_dir)

    platforms_dir = os.path.join(run_dir, 'code', 'platforms')
    os.makedirs(platforms_dir, exist_ok=True)

    generated_files: List[str] = []

    adapter_map = {
        'alpaca': ('alpaca_adapter.py', _alpaca_adapter_source),
        'binance': ('binance_adapter.py', _binance_adapter_source),
        'ibkr': ('ibkr_adapter.py', _ibkr_adapter_source),
    }

    for target in targets:
        if target not in adapter_map:
            continue
        filename, source_fn = adapter_map[target]
        source = source_fn(paper_mode)
        fpath = os.path.join(platforms_dir, filename)
        _tmp_fpath = fpath + ".tmp"
        try:
            with open(_tmp_fpath, 'w', encoding='utf-8') as fh:
                fh.write(source)
            os.replace(_tmp_fpath, fpath)
            generated_files.append(fpath)
        except OSError:
            try:
                os.unlink(_tmp_fpath)
            except OSError:
                pass

    # Write __init__.py to make it a package (atomic exclusive-create avoids TOCTOU race)
    init_path = os.path.join(platforms_dir, '__init__.py')
    try:
        with open(init_path, 'x', encoding='utf-8') as fh:
            fh.write('"""Auto-generated trading platform adapters."""\n')
    except FileExistsError:
        pass
    except OSError:
        pass

    report: Dict[str, Any] = {
        'strategy_type': strategy_type,
        'targets': targets,
        'generated_files': generated_files,
        'paper_mode': paper_mode,
        'safety_flags': {
            'paper_trading_guard': paper_mode,
            'min_order_interval_s': 5.0,
            'max_position_pct': 0.05,
        },
    }

    report_path = os.path.join(run_dir, 'trading_platform_report.json')
    _tmp_rpt = report_path + ".tmp"
    try:
        with open(_tmp_rpt, 'w', encoding='utf-8') as fh:
            json.dump(report, fh, indent=2)
        os.replace(_tmp_rpt, report_path)
    except OSError:
        try:
            os.unlink(_tmp_rpt)
        except OSError:
            pass

    return report


