"""
features/trading_platform.py
==============================
Trading platform adapter stub generator for Alpaca, Binance, and IBKR.

Detects the strategy type from ``analysis_result.json`` (equity / crypto /
multi-asset) and writes three ready-to-use adapter Python files into
``{run_dir}/code/platforms/``:

* ``alpaca_adapter.py``   — Alpaca REST v2 with paper-trading safety checks.
* ``binance_adapter.py``  — Binance REST v3 with rate limiting.
* ``ibkr_adapter.py``     — Interactive Brokers IBAPI stub.

All adapters share a common interface:

* ``place_order(symbol, qty, side, order_type, limit_price)``
* ``get_position(symbol)``
* ``get_account_balance()``
* ``cancel_all_orders()``

Safety constraints applied to every adapter:

* PAPER_TRADING flag guard — live orders are blocked unless explicitly enabled.
* Rate limiter — minimum 5 seconds between consecutive order submissions.
* Position size cap — each order is capped at 5 % of reported account equity.

Writes ``trading_platform_report.json`` with strategy type, generated files,
and applicable safety flags.

Environment variables
---------------------
TRADING_PLATFORM_TARGETS   Comma-separated list of adapter targets to generate.
                            Supported: alpaca, binance, ibkr (default: 'alpaca,binance').
TRADING_PAPER_MODE         'true' to block live order submission (default: 'true').
"""
from __future__ import annotations

import json
import os
import textwrap
import time
from typing import Any, Dict, List, Optional

from crucible.feature_registry import BaseFeature, FeatureConfig, FeatureResult, register

# ---------------------------------------------------------------------------
# Strategy type detection
# ---------------------------------------------------------------------------

_CRYPTO_KEYWORDS = frozenset([
    'btc', 'eth', 'bnb', 'sol', 'xrp', 'crypto', 'binance',
    'defi', 'blockchain', 'usdt', 'usdc',
])
_EQUITY_KEYWORDS = frozenset([
    'equity', 'stock', 'shares', 'alpaca', 'nasdaq', 'nyse', 'spy',
    'qqq', 'iwm', 'sector', 'dividend',
])


def detect_strategy_type(run_dir: str) -> str:
    """Return 'crypto', 'equity', or 'multi-asset' by scanning analysis_result.json."""
    analysis_path = os.path.join(run_dir, 'analysis_result.json')
    if not os.path.isfile(analysis_path):
        return 'multi-asset'
    try:
        with open(analysis_path, 'r', encoding='utf-8') as fh:
            text = fh.read().lower()
    except OSError:
        return 'multi-asset'

    has_crypto = any(kw in text for kw in _CRYPTO_KEYWORDS)
    has_equity = any(kw in text for kw in _EQUITY_KEYWORDS)

    if has_crypto and has_equity:
        return 'multi-asset'
    if has_crypto:
        return 'crypto'
    return 'equity'


